use crate::proposer::{Proposal, ProposalKind};

/// A confidence score with a breakdown of contributing factors.
#[derive(Debug, Clone, PartialEq)]
pub struct ConfidenceScore {
    /// Overall confidence in [0.0, 1.0].
    pub value: f64,
    /// Breakdown of individual factors that contributed to the score.
    pub breakdown: ConfidenceBreakdown,
}

impl ConfidenceScore {
    /// Create a new confidence score, clamping value to [0.0, 1.0].
    #[must_use]
    pub fn new(value: f64, breakdown: ConfidenceBreakdown) -> Self {
        Self { value: value.clamp(0.0, 1.0), breakdown }
    }
}

/// Breakdown of factors contributing to a confidence score.
#[derive(Debug, Clone, PartialEq)]
pub struct ConfidenceBreakdown {
    /// Reliability of the proposal source (heuristic, WP, LLM, etc.).
    pub source_reliability: f64,
    /// Score based on formula complexity (simpler = higher).
    pub formula_complexity: f64,
    /// How many counterexamples this spec addresses.
    pub counterexample_coverage: f64,
    /// Historical success rate of similar specs.
    pub historical_success: f64,
}

impl Default for ConfidenceBreakdown {
    fn default() -> Self {
        Self {
            source_reliability: 0.5,
            formula_complexity: 0.5,
            counterexample_coverage: 0.0,
            historical_success: 0.5,
        }
    }
}

/// The source that generated a proposal, used for source reliability scoring.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
#[non_exhaustive]
pub enum ProposalSource {
    /// Generated by heuristic pattern matching.
    Heuristic,
    /// Generated by weakest precondition calculus.
    WeakestPrecondition,
    /// Generated by LLM inference.
    Llm,
    /// Generated by counterexample analysis.
    CounterexampleGuided,
    /// Generated by signature-based inference.
    SignatureBased,
}

impl ProposalSource {
    /// Default reliability score for this source.
    #[must_use]
    pub fn default_reliability(self) -> f64 {
        match self {
            Self::Heuristic => 0.6,
            Self::WeakestPrecondition => 0.8,
            Self::Llm => 0.5,
            Self::CounterexampleGuided => 0.75,
            Self::SignatureBased => 0.4,
        }
    }
}

/// Configurable weights for combining confidence factors.
#[derive(Debug, Clone, PartialEq)]
pub struct ConfidenceWeights {
    /// Weight for source reliability factor.
    pub source_reliability: f64,
    /// Weight for formula complexity factor.
    pub formula_complexity: f64,
    /// Weight for counterexample coverage factor.
    pub counterexample_coverage: f64,
    /// Weight for historical success factor.
    pub historical_success: f64,
}

impl Default for ConfidenceWeights {
    fn default() -> Self {
        Self {
            source_reliability: 0.35,
            formula_complexity: 0.20,
            counterexample_coverage: 0.25,
            historical_success: 0.20,
        }
    }
}

impl ConfidenceWeights {
    /// Normalize weights so they sum to 1.0.
    #[must_use]
    pub fn normalized(&self) -> Self {
        let total = self.source_reliability
            + self.formula_complexity
            + self.counterexample_coverage
            + self.historical_success;
        if total <= f64::EPSILON {
            return Self::default();
        }
        Self {
            source_reliability: self.source_reliability / total,
            formula_complexity: self.formula_complexity / total,
            counterexample_coverage: self.counterexample_coverage / total,
            historical_success: self.historical_success / total,
        }
    }
}

/// Combines multiple factors into a single confidence score.
pub struct ConfidenceEstimator {
    /// Weights for combining factors.
    weights: ConfidenceWeights,
    /// Override reliability scores per source.
    source_overrides: FxHashMap<ProposalSource, f64>,
    /// Historical success tracker for calibration.
    calibration: CalibrationTracker,
}

impl ConfidenceEstimator {
    /// Create a new estimator with default weights.
    #[must_use]
    pub fn new() -> Self {
        Self {
            weights: ConfidenceWeights::default(),
            source_overrides: FxHashMap::default(),
            calibration: CalibrationTracker::new(),
        }
    }

    /// Create an estimator with custom weights.
    #[must_use]
    pub fn with_weights(weights: ConfidenceWeights) -> Self {
        Self {
            weights,
            source_overrides: FxHashMap::default(),
            calibration: CalibrationTracker::new(),
        }
    }

    /// Override the reliability score for a specific source.
    pub fn set_source_reliability(&mut self, source: ProposalSource, reliability: f64) {
        self.source_overrides.insert(source, reliability.clamp(0.0, 1.0));
    }

    /// Score a proposal given its source and context.
    #[must_use]
    pub fn score(
        &self,
        proposal: &Proposal,
        source: ProposalSource,
        counterexamples_addressed: usize,
        total_counterexamples: usize,
    ) -> ConfidenceScore {
        let weights = self.weights.normalized();

        let source_reliability = self
            .source_overrides
            .get(&source)
            .copied()
            .unwrap_or_else(|| source.default_reliability());

        let formula_complexity = score_complexity(&proposal.kind);

        let counterexample_coverage = if total_counterexamples > 0 {
            (counterexamples_addressed as f64) / (total_counterexamples as f64)
        } else {
            0.5 // neutral when no counterexamples available
        };

        let historical_success =
            self.calibration.success_rate_for_kind(&proposal.kind).unwrap_or(0.5);

        let breakdown = ConfidenceBreakdown {
            source_reliability,
            formula_complexity,
            counterexample_coverage,
            historical_success,
        };

        let value = weights.source_reliability * source_reliability
            + weights.formula_complexity * formula_complexity
            + weights.counterexample_coverage * counterexample_coverage
            + weights.historical_success * historical_success;

        ConfidenceScore::new(value, breakdown)
    }

    /// Record whether a proposal succeeded or failed, for calibration.
    pub fn record_outcome(&mut self, proposal: &Proposal, succeeded: bool) {
        self.calibration.record(&proposal.kind, succeeded);
    }

    /// Get the current calibration tracker (read-only).
    #[must_use]
    pub fn calibration(&self) -> &CalibrationTracker {
        &self.calibration
    }
}

impl Default for ConfidenceEstimator {
    fn default() -> Self {
        Self::new()
    }
}

/// Scoring strategy for ranking proposals.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[non_exhaustive]
pub enum RankingStrategy {
    /// Rank by overall confidence score (highest first).
    ByConfidence,
    /// Rank by formula simplicity (simpler specs first).
    BySimplicity,
    /// Rank by expected impact (addresses more counterexamples first).
    ByImpact,
    /// Combined ranking: weighted blend of confidence, simplicity, and impact.
    Combined,
}

/// A proposal paired with its computed confidence score.
#[derive(Debug, Clone)]
pub struct ScoredProposal {
    /// The original proposal.
    pub proposal: Proposal,
    /// The computed confidence score.
    pub score: ConfidenceScore,
    /// Number of counterexamples this proposal addresses.
    pub counterexamples_addressed: usize,
}

/// Rank proposals according to a strategy.
///
/// Returns a new sorted vector with scored proposals. The input is not modified.
#[must_use]
pub fn rank_proposals(scored: &[ScoredProposal], strategy: RankingStrategy) -> Vec<ScoredProposal> {
    let mut sorted: Vec<ScoredProposal> = scored.to_vec();
    match strategy {
        RankingStrategy::ByConfidence => {
            sorted.sort_by(|a, b| {
                b.score.value.partial_cmp(&a.score.value).unwrap_or(std::cmp::Ordering::Equal)
            });
        }
        RankingStrategy::BySimplicity => {
            sorted.sort_by(|a, b| {
                b.score
                    .breakdown
                    .formula_complexity
                    .partial_cmp(&a.score.breakdown.formula_complexity)
                    .unwrap_or(std::cmp::Ordering::Equal)
            });
        }
        RankingStrategy::ByImpact => {
            sorted.sort_by(|a, b| {
                b.counterexamples_addressed.cmp(&a.counterexamples_addressed).then_with(|| {
                    b.score.value.partial_cmp(&a.score.value).unwrap_or(std::cmp::Ordering::Equal)
                })
            });
        }
        RankingStrategy::Combined => {
            sorted.sort_by(|a, b| {
                let a_combined = combined_rank_score(a);
                let b_combined = combined_rank_score(b);
                b_combined.partial_cmp(&a_combined).unwrap_or(std::cmp::Ordering::Equal)
            });
        }
    }
    sorted
}

/// Compute a combined ranking score blending confidence, simplicity, and impact.
fn combined_rank_score(sp: &ScoredProposal) -> f64 {
    let confidence_weight = 0.5;
    let simplicity_weight = 0.2;
    let impact_weight = 0.3;

    let impact_normalized = (sp.counterexamples_addressed as f64).min(10.0) / 10.0;

    confidence_weight * sp.score.value
        + simplicity_weight * sp.score.breakdown.formula_complexity
        + impact_weight * impact_normalized
}

/// Score the complexity of a proposal's formula. Simpler specs score higher.
fn score_complexity(kind: &ProposalKind) -> f64 {
    let spec_text = match kind {
        ProposalKind::AddPrecondition { spec_body } => spec_body.as_str(),
        ProposalKind::AddPostcondition { spec_body } => spec_body.as_str(),
        ProposalKind::AddInvariant { spec_body } => spec_body.as_str(),
        ProposalKind::SafeArithmetic { replacement, .. } => replacement.as_str(),
        ProposalKind::AddBoundsCheck { check_expr } => check_expr.as_str(),
        ProposalKind::AddNonZeroCheck { check_expr } => check_expr.as_str(),
    };

    // Heuristic: shorter specs are simpler. Also penalize logical connectives.
    let len_score = 1.0 - (spec_text.len() as f64 / 200.0).min(1.0);

    let connective_count = spec_text.matches("&&").count()
        + spec_text.matches("||").count()
        + spec_text.matches("==>").count()
        + spec_text.matches("forall").count()
        + spec_text.matches("exists").count();
    let connective_penalty = (connective_count as f64 * 0.1).min(0.5);

    (len_score - connective_penalty).clamp(0.0, 1.0)
}

/// Categorize a ProposalKind into a string key for calibration tracking.
fn kind_category(kind: &ProposalKind) -> &'static str {
    match kind {
        ProposalKind::AddPrecondition { .. } => "precondition",
        ProposalKind::AddPostcondition { .. } => "postcondition",
        ProposalKind::AddInvariant { .. } => "invariant",
        ProposalKind::SafeArithmetic { .. } => "safe_arithmetic",
        ProposalKind::AddBoundsCheck { .. } => "bounds_check",
        ProposalKind::AddNonZeroCheck { .. } => "nonzero_check",
    }
}

/// Tracks predicted vs actual success rates for calibration.
#[derive(Debug, Clone)]
pub struct CalibrationTracker {
    /// Per-category: (successes, total attempts).
    records: FxHashMap<&'static str, (u64, u64)>,
}

impl CalibrationTracker {
    /// Create a new empty tracker.
    #[must_use]
    pub fn new() -> Self {
        Self { records: FxHashMap::default() }
    }

    /// Record an outcome for a proposal kind.
    pub fn record(&mut self, kind: &ProposalKind, succeeded: bool) {
        let category = kind_category(kind);
        let entry = self.records.entry(category).or_insert((0, 0));
        if succeeded {
            entry.0 += 1;
        }
        entry.1 += 1;
    }

    /// Get the success rate for a proposal kind, if any data exists.
    #[must_use]
    pub fn success_rate_for_kind(&self, kind: &ProposalKind) -> Option<f64> {
        let category = kind_category(kind);
        self.records.get(category).and_then(|&(successes, total)| {
            if total > 0 { Some(successes as f64 / total as f64) } else { None }
        })
    }

    /// Get success rate for a named category.
    #[must_use]
    pub fn success_rate(&self, category: &str) -> Option<f64> {
        self.records.get(category).and_then(|&(successes, total)| {
            if total > 0 { Some(successes as f64 / total as f64) } else { None }
        })
    }

    /// Get the total number of recorded outcomes.
    #[must_use]
    pub fn total_records(&self) -> u64 {
        self.records.values().map(|&(_, total)| total).sum()
    }

    /// Get all categories with their success rates.
    #[must_use]
    pub fn all_rates(&self) -> Vec<(&'static str, f64)> {
        self.records
            .iter()
            .filter_map(
                |(&cat, &(successes, total))| {
                    if total > 0 { Some((cat, successes as f64 / total as f64)) } else { None }
                },
            )
            .collect()
    }
}

impl Default for CalibrationTracker {
    fn default() -> Self {
        Self::new()
    }
}

