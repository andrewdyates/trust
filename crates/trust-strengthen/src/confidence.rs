    // --- RankingStrategy ---

    fn make_scored(spec: &str, confidence: f64, cex: usize) -> ScoredProposal {
        let proposal = precondition_proposal(spec, confidence);
        let complexity = score_complexity(&proposal.kind);
        ScoredProposal {
            proposal,
            score: ConfidenceScore::new(
                confidence,
                ConfidenceBreakdown {
                    source_reliability: 0.6,
                    formula_complexity: complexity,
                    counterexample_coverage: 0.5,
                    historical_success: 0.5,
                },
            ),
            counterexamples_addressed: cex,
        }
    }

