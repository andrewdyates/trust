    #[test]
    fn test_verdict_stalled_same_frontier_3_rounds() {
        // Same frontier with failures for 3 rounds => Stalled verdict
        // Need stable_round_limit high so Converged isn't triggered, but
        // stall_threshold=3 fires first
        let verify = MockVerify::new(vec![
            verify_output(3, 2),
            verify_output(3, 2),
            verify_output(3, 2),
            verify_output(3, 2),
        ]);
        let strengthen = MockStrengthen { proposal_count: 1 };
        let backprop = MockBackprop;
        let config = VerificationLoopConfig {
            max_iterations: 10,
            stable_round_limit: 5, // high so we don't converge via decision
            run_debug: false,
            ..Default::default()
        };

        let mut vloop =
            VerificationLoop::new(config, &verify, &strengthen, &backprop);

        // Run 3 iterations manually
        let _ = vloop.iterate(&test_path()); // iter 0: first, Continue
        let _ = vloop.iterate(&test_path()); // iter 1: stable
        let _ = vloop.iterate(&test_path()); // iter 2: stable

        // After 3 observations with same frontier, verdict should be Stalled
        let verdict = vloop.convergence_verdict();
        assert_eq!(
            verdict,
            trust_convergence::ConvergenceVerdict::Stalled { stale_iterations: 3 }
        );
    }

