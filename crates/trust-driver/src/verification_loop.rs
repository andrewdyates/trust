use std::path::Path;
use std::time::{Duration, Instant};

use crate::{
    BackpropOutput, BackpropPhase, ConvergenceDecision, ConvergenceTracker, DebugOutput,
    DebugPhase, IterationSnapshot, ProofFrontier, StrengthenPhase, VerifyOutput, VerifyPhase,
};

// ---------------------------------------------------------------------------
// LoopOutcome — the four possible outcomes of the verification loop
// ---------------------------------------------------------------------------

/// High-level outcome of running the iterative verification loop.
#[derive(Debug, Clone, PartialEq, Eq)]
#[non_exhaustive]
pub(crate) enum LoopOutcome {
    /// All verification conditions were discharged — the code is verified.
    Verified {
        /// Number of iterations it took.
        iterations: u32,
        /// Total VCs proved across all functions.
        vcs_proved: usize,
    },
    /// A real counterexample was found — the code has a bug (or specs are wrong).
    Refuted {
        /// Number of iterations before giving up.
        iterations: u32,
        /// Number of VCs that remain unproved.
        vcs_failed: usize,
        /// Whether strengthen ran out of proposals.
        exhausted_proposals: bool,
    },
    /// The loop hit its iteration limit without converging.
    Timeout {
        /// Iteration count at termination.
        iterations: u32,
        /// The configured maximum.
        max_iterations: u32,
        /// Final proof frontier.
        final_frontier: ProofFrontier,
    },
    /// The proof frontier regressed or oscillated — the loop is diverging.
    Diverged {
        /// Iteration at which regression was detected.
        iterations: u32,
        /// Description of what regressed.
        reason: String,
    },
}

// ---------------------------------------------------------------------------
// IterationSummary — per-step reporting
// ---------------------------------------------------------------------------

/// Summary of a single iteration of the verification loop.
#[derive(Debug, Clone)]
pub(crate) struct IterationSummary {
    /// Zero-based iteration index.
    pub iteration: u32,
    /// Proof frontier after this iteration's verify phase.
    pub frontier: ProofFrontier,
    /// Number of VCs that failed verification.
    pub vcs_failed: usize,
    /// Number of VCs dispatched to solvers.
    pub vcs_dispatched: usize,
    /// Number of spec proposals generated by strengthen.
    pub proposals_generated: usize,
    /// Number of proposals successfully applied by backprop.
    pub proposals_applied: usize,
    /// Convergence decision from the tracker.
    pub convergence: ConvergenceDecision,
    /// Debug violations found (if debug phase enabled).
    pub debug_violations: usize,
    /// Whether any critical/high debug violations were found.
    pub debug_critical: bool,
    /// Wall-clock time for this iteration.
    pub elapsed: Duration,
}

// ---------------------------------------------------------------------------
// VerificationLoopConfig
// ---------------------------------------------------------------------------

/// Configuration for the iterative verification loop.
#[derive(Debug, Clone)]
pub(crate) struct VerificationLoopConfig {
    /// Maximum number of iterations before returning `Timeout`.
    pub max_iterations: u32,
    /// Number of consecutive stable rounds before declaring convergence.
    pub stable_round_limit: usize,
    /// Whether backprop is allowed to modify source files.
    pub allow_rewrite: bool,
    /// Whether to run the debug phase (violation discovery) each iteration.
    pub run_debug: bool,
    /// Whether to use incremental verification caching. Set to `false` with
    /// `--no-cache` to force re-verification of all functions.
    pub use_cache: bool,
}

impl Default for VerificationLoopConfig {
    fn default() -> Self {
        Self {
            max_iterations: 8,
            stable_round_limit: 2,
            allow_rewrite: true,
            run_debug: true,
            use_cache: true,
        }
    }
}

impl From<&crate::LoopConfig> for VerificationLoopConfig {
    fn from(lc: &crate::LoopConfig) -> Self {
        Self {
            max_iterations: lc.max_iterations,
            stable_round_limit: lc.stable_round_limit,
            allow_rewrite: lc.allow_rewrite,
            run_debug: lc.run_debug,
            use_cache: true, // LoopConfig does not have this field; default on.
        }
    }
}

