/// Iterative verification loop for the native MIR pass.
///
/// Orchestrates repeated cycles of:
///   1. **Verify** — extract MIR, generate VCs, dispatch to solvers
///   2. **Debug** (optional) — find violations the verify phase may miss
///   3. **Analyze** — check convergence via `ConvergenceTracker`
///   4. **Strengthen** — propose specs from failure patterns
///   5. **Backprop** — apply proposed specs to source
///
/// Stops when all VCs pass, the frontier converges, the loop diverges (regression),
/// or the iteration limit is reached.
pub(crate) struct VerificationLoop<'a> {
    config: VerificationLoopConfig,
    verify: &'a dyn VerifyPhase,
    strengthen: &'a dyn StrengthenPhase,
    backprop: &'a dyn BackpropPhase,
    debug: Option<&'a dyn DebugPhase>,
    tracker: ConvergenceTracker,
    history: Vec<IterationSummary>,
    iteration: u32,
}

impl<'a> VerificationLoop<'a> {
    /// Create a new verification loop with the given phases and config.
    #[must_use]
    pub(crate) fn new(
        config: VerificationLoopConfig,
        verify: &'a dyn VerifyPhase,
        strengthen: &'a dyn StrengthenPhase,
        backprop: &'a dyn BackpropPhase,
    ) -> Self {
        let tracker =
            ConvergenceTracker::new(config.stable_round_limit, config.max_iterations);
        Self {
            config,
            verify,
            strengthen,
            backprop,
            debug: None,
            tracker,
            history: Vec::new(),
            iteration: 0,
        }
    }

    /// Attach an optional debug phase for violation discovery.
    #[must_use]
    pub(crate) fn with_debug(mut self, debug: &'a dyn DebugPhase) -> Self {
        self.debug = Some(debug);
        self
    }

    /// Run the full iterative loop to completion.
    ///
    /// Returns the high-level `LoopOutcome` plus the per-iteration history.
    pub(crate) fn run(&mut self, source_path: &Path) -> (LoopOutcome, Vec<IterationSummary>) {
        loop {
            let outcome = self.iterate(source_path);
            if let Some(outcome) = outcome {
                let history = std::mem::take(&mut self.history);
                return (outcome, history);
            }
        }
    }

    /// Execute a single iteration of the verification loop.
    ///
    /// Returns `Some(LoopOutcome)` if the loop should terminate, or `None` if
    /// another iteration is needed. This enables callers to inspect intermediate
    /// state between iterations or implement custom termination logic.
    pub(crate) fn iterate(&mut self, source_path: &Path) -> Option<LoopOutcome> {
        let start = Instant::now();

        // Phase 1: Verify — extract MIR, generate VCs, dispatch to solvers
        let v_out = self.verify.verify(source_path);

        // Phase 2: Debug — find violations the verify phase may miss
        let d_out = if self.config.run_debug {
            self.debug.map(|d| d.find_violations(source_path))
        } else {
            None
        };
        let (debug_violations, debug_critical) = extract_debug_stats(&d_out);

        // Phase 3: Analyze — check convergence
        let snapshot = build_snapshot(self.iteration, &v_out);
        let decision = self.tracker.observe(snapshot);

        // Early exit: all VCs proved and no critical debug violations
        if v_out.vcs_failed == 0 && !debug_critical {
            self.push_summary(
                &v_out, 0, 0, &decision, debug_violations, debug_critical, start,
            );
            return Some(LoopOutcome::Verified {
                iterations: self.iteration + 1,
                vcs_proved: v_out.vcs_dispatched,
            });
        }

        // Check convergence/regression/limit
        match &decision {
            ConvergenceDecision::Converged { .. } => {
                self.push_summary(
                    &v_out, 0, 0, &decision, debug_violations, debug_critical, start,
                );
                // Converged but still has failures => Refuted
                return Some(LoopOutcome::Refuted {
                    iterations: self.iteration + 1,
                    vcs_failed: v_out.vcs_failed,
                    exhausted_proposals: false,
                });
            }
            ConvergenceDecision::IterationLimitReached { limit, .. } => {
                self.push_summary(
                    &v_out, 0, 0, &decision, debug_violations, debug_critical, start,
                );
                return Some(LoopOutcome::Timeout {
                    iterations: self.iteration + 1,
                    max_iterations: *limit,
                    final_frontier: v_out.frontier,
                });
            }
            ConvergenceDecision::Regressed { reason } => {
                self.push_summary(
                    &v_out, 0, 0, &decision, debug_violations, debug_critical, start,
                );
                return Some(LoopOutcome::Diverged {
                    iterations: self.iteration + 1,
                    reason: format!("{reason:?}"),
                });
            }
            ConvergenceDecision::Continue { .. } => {
                // Continue to strengthen + backprop
            }
        }

        // Phase 4: Strengthen — analyze failures and propose specs
        let s_out = self.strengthen.strengthen(source_path, &v_out);

        if !s_out.has_proposals {
            self.push_summary(
                &v_out, 0, 0, &decision, debug_violations, debug_critical, start,
            );
            return Some(LoopOutcome::Refuted {
                iterations: self.iteration + 1,
                vcs_failed: v_out.vcs_failed,
                exhausted_proposals: true,
            });
        }

        // Phase 5: Backprop — apply proposed specs to source
        let b_out = if self.config.allow_rewrite {
            self.backprop.backpropagate(source_path, &s_out.proposals)
        } else {
            BackpropOutput {
                applied: 0,
                skipped: s_out.proposals.len(),
            }
        };

        self.push_summary(
            &v_out,
            s_out.proposals.len(),
            b_out.applied,
            &decision,
            debug_violations,
            debug_critical,
            start,
        );

        // If rewrite enabled but nothing applied, treat as exhausted
        if self.config.allow_rewrite && b_out.applied == 0 {
            return Some(LoopOutcome::Refuted {
                iterations: self.iteration + 1,
                vcs_failed: v_out.vcs_failed,
                exhausted_proposals: true,
            });
        }

        self.iteration += 1;
        None // continue looping
    }

    /// Access the iteration history so far.
    #[must_use]
    pub(crate) fn history(&self) -> &[IterationSummary] {
        &self.history
    }

    /// Current iteration index (zero-based).
    #[must_use]
    pub(crate) fn current_iteration(&self) -> u32 {
        self.iteration
    }

    /// Access the convergence tracker for detailed convergence state.
    #[must_use]
    pub(crate) fn tracker(&self) -> &ConvergenceTracker {
        &self.tracker
    }

    /// Get the high-level convergence verdict from the tracker.
    ///
    /// This summarizes the overall trajectory: Converged, Stalled, Regressed,
    /// or InProgress.
    #[must_use]
    pub(crate) fn convergence_verdict(&self) -> trust_convergence::ConvergenceVerdict {
        self.tracker.verdict()
    }

    #[allow(clippy::too_many_arguments)]
    fn push_summary(
        &mut self,
        v_out: &VerifyOutput,
        proposals: usize,
        applied: usize,
        decision: &ConvergenceDecision,
        debug_violations: usize,
        debug_critical: bool,
        start: Instant,
    ) {
        self.history.push(IterationSummary {
            iteration: self.iteration,
            frontier: v_out.frontier.clone(),
            vcs_failed: v_out.vcs_failed,
            vcs_dispatched: v_out.vcs_dispatched,
            proposals_generated: proposals,
            proposals_applied: applied,
            convergence: decision.clone(),
            debug_violations,
            debug_critical,
            elapsed: start.elapsed(),
        });
    }
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

fn extract_debug_stats(d_out: &Option<DebugOutput>) -> (usize, bool) {
    match d_out {
        Some(d) => (d.new_proof_obligations, d.has_critical),
        None => (0, false),
    }
}

fn build_snapshot(iteration: u32, v_out: &VerifyOutput) -> IterationSnapshot {
    match &v_out.fingerprint {
        Some(fp) => {
            IterationSnapshot::new(iteration, v_out.frontier.clone()).with_fingerprint(fp)
        }
        None => IterationSnapshot::new(iteration, v_out.frontier.clone()),
    }
}

