// trust-driver/verification_loop.rs: Iterative verification loop for the native MIR pass.
//
// Wraps the extract -> vcgen -> dispatch -> analyze -> strengthen cycle into a
// `VerificationLoop` struct with an `iterate()` method. Uses trust-convergence
// for fixed-point detection and trust-strengthen/trust-backprop for spec
// inference and source feedback.
//
