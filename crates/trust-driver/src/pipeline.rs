/// Default CEGAR stage that invokes the trust-cegar refinement loop.
///
/// When VCs fail, builds a synthetic counterexample from the failure count,
/// runs the CegarLoop to check spuriousness and refine predicates, and
/// adjusts the VerifyOutput accordingly. If CEGAR proves safety via
/// abstraction, failed VCs are promoted to trusted.
///
/// tRust: Wired as part of #523 (CEGAR pipeline integration).
pub(crate) struct DefaultCegarStage {
    /// CEGAR configuration for this stage.
    config: CegarPhaseConfig,
}

impl DefaultCegarStage {
    /// Create a new default CEGAR stage with the given config.
    #[must_use]
    pub(crate) fn new(config: CegarPhaseConfig) -> Self {
        Self { config }
    }
}

impl CegarStage for DefaultCegarStage {
    fn refine(&self, _source_path: &Path, mut output: VerifyOutput) -> (VerifyOutput, CegarStats) {
        // If no failures, skip CEGAR entirely.
        if output.vcs_failed == 0 {
            return (output, CegarStats::default());
        }

        // tRust #523: Wire the actual CEGAR refinement loop.
        let mut stats = CegarStats::default();
        let mut cegar_loop = CegarLoop::new(
            self.config.initial_predicates.clone(),
            CegarConfig {
                max_iterations: self.config.max_cegar_iterations,
            },
        );
        let mut refiner = LazyRefiner::new(self.config.initial_predicates.clone());

        // Use blocks from config if provided.
        let blocks = &self.config.blocks;

        // CEGAR refinement iterations: try to eliminate spurious counterexamples.
        for _ in 0..self.config.max_cegar_iterations {
            if output.vcs_failed == 0 {
                break;
            }

            // Build a synthetic counterexample from the failure count.
            let cex = Counterexample::new(vec![
                (
                    "vcs_failed".to_string(),
                    CounterexampleValue::Uint(output.vcs_failed as u128),
                ),
                (
                    "vcs_total".to_string(),
                    CounterexampleValue::Uint(output.vcs_dispatched as u128),
                ),
            ]);

            // Compute abstraction for current blocks if available.
            if !blocks.is_empty() {
                cegar_loop.compute_abstraction(blocks);
            }

            // Check if the counterexample is spurious.
            let is_spurious = cegar_loop.is_spurious(&cex, blocks);

            if !is_spurious {
                stats.real_counterexamples += 1;
                break;
            }

            stats.spurious_eliminated += 1;

            // Attempt refinement via the CegarLoop.
            let outcome = cegar_loop.refine_iteration(&cex, blocks);
            stats.refinement_iterations += 1;

            match outcome {
                Ok(CegarOutcome::Refined { new_predicates }) => {
                    stats.predicates_added += new_predicates.len();

                    // Also refine along the path using LazyRefiner.
                    let path = extract_linear_block_path(blocks);
                    if !path.is_empty() {
                        if let Ok(lazy_preds) = refiner.refine_path(&path, blocks, &cex) {
                            stats.predicates_added += lazy_preds.len();
                        }
                    }
                    // Note: re-verification would happen in the next pipeline
                    // iteration -- we adjust the output optimistically here
                    // only when CEGAR proves full safety.
                }
                Ok(CegarOutcome::Safe) => {
                    // Abstraction proves the property -- promote failed to trusted.
                    let promoted = output.vcs_failed;
                    output.vcs_failed = 0;
                    output.frontier.trusted += promoted as u32;
                    output.frontier.failed = 0;
                    stats.spurious_eliminated += promoted;
                    break;
                }
                Ok(CegarOutcome::Unsafe) => {
                    stats.real_counterexamples += 1;
                    break;
                }
                Err(trust_cegar::CegarError::MaxIterationsExceeded { .. }) => {
                    stats.hit_limit = true;
                    break;
                }
                Err(trust_cegar::CegarError::RefinementStalled) => {
                    // No new predicates -- cannot make further progress.
                    break;
                }
                // CegarOutcome is #[non_exhaustive]; future variants stop refinement.
                Ok(_) => break,
                Err(_) => break,
            }
        }

        (output, stats)
    }
}

/// Extract a linear block path from basic blocks by following Goto terminators.
fn extract_linear_block_path(blocks: &[BasicBlock]) -> Vec<usize> {
    if blocks.is_empty() {
        return Vec::new();
    }

    let mut path = Vec::new();
    let mut visited = vec![false; blocks.len()];
    let mut current = 0usize;

    loop {
        if current >= blocks.len() || visited[current] {
            break;
        }
        visited[current] = true;
        path.push(current);

        match &blocks[current].terminator {
            Terminator::Goto(BlockId(next)) => {
                current = *next;
            }
            _ => break,
        }
    }

    path
}

/// Default convergence stage backed by trust-convergence's ConvergenceTracker.
pub(crate) struct DefaultConvergenceStage {
    tracker: ConvergenceTracker,
}

impl DefaultConvergenceStage {
    /// Create a new convergence stage with the given limits.
    #[must_use]
    pub(crate) fn new(stable_round_limit: usize, iteration_limit: u32, stall_threshold: usize) -> Self {
        let tracker = ConvergenceTracker::new(stable_round_limit, iteration_limit)
            .with_verdict_config(VerdictConfig::new(stall_threshold));
        Self { tracker }
    }
}

impl ConvergenceStage for DefaultConvergenceStage {
    fn observe(
        &mut self,
        iteration: u32,
        output: &VerifyOutput,
    ) -> ConvergenceDecision {
        let snapshot = match &output.fingerprint {
            Some(fp) => {
                IterationSnapshot::new(iteration, output.frontier.clone()).with_fingerprint(fp)
            }
            None => IterationSnapshot::new(iteration, output.frontier.clone()),
        };
        self.tracker.observe(snapshot)
    }

    fn verdict(&self) -> ConvergenceVerdict {
        self.tracker.verdict()
    }

    fn consecutive_stable_count(&self) -> usize {
        self.tracker.consecutive_stable_count()
    }
}

/// Default certification stage that generates proof certificates via
/// `trust_proof_cert::generate_certificate` and then attempts lean5 kernel
/// certification via `trust_lean5::CertificationPipeline`.
///
/// When `detailed_results` are available in the VerifyOutput, iterates over
/// each function's (VC, result) pairs and generates a `GeneratedCertificate`
/// for functions that have at least one proved VC. For proved VCs that carry
/// a `proof_certificate` (z4 proof bytes), the stage also attempts lean5
/// certification to upgrade Trusted -> Certified.
///
/// Falls back to counting proved VCs from the frontier when detailed results
/// are unavailable.
///
/// tRust: Wired as part of #523 (proof certification pipeline integration).
/// tRust #545: lean5 certification wired into the pipeline.
pub(crate) struct DefaultCertificationStage {
    /// tRust #545: lean5 certification pipeline for upgrading Trusted -> Certified.
    lean5_pipeline: trust_lean5::CertificationPipeline,
}

impl DefaultCertificationStage {
    /// Create a new default certification stage with lean5 kernel certification.
    #[must_use]
    pub(crate) fn new() -> Self {
        Self {
            lean5_pipeline: trust_lean5::CertificationPipeline::new(),
        }
    }
}

impl Default for DefaultCertificationStage {
    fn default() -> Self {
        Self::new()
    }
}

impl CertificationStage for DefaultCertificationStage {
    fn certify(&self, _source_path: &Path, output: &VerifyOutput) -> CertificationSummary {
        let start = Instant::now();

        // tRust #523: Use detailed_results to generate real certificates when available.
        if let Some(ref detailed) = output.detailed_results {
            let mut generated = 0usize;
            let mut skipped = 0usize;
            let mut lean5_certified = 0usize;
            let mut lean5_rejected = 0usize;

            for func_result in &detailed.functions {
                if func_result.results.is_empty() {
                    continue;
                }

                // Check if any VC in this function was proved.
                let has_proved = func_result
                    .results
                    .iter()
                    .any(|(_, r)| r.is_proved());

                if !has_proved {
                    skipped += func_result.results.len();
                    continue;
                }

                // Build a minimal VerifiableFunction for certificate generation.
                // We need def_path, name, and empty body/spec -- the certificate
                // generator primarily uses the (VC, result) pairs.
                let func = trust_types::VerifiableFunction {
                    name: func_result.function_name.clone(),
                    def_path: func_result.function_path.clone(),
                    span: trust_types::SourceSpan::default(),
                    body: trust_types::VerifiableBody {
                        locals: Vec::new(),
                        blocks: Vec::new(),
                        arg_count: 0,
                        return_ty: trust_types::Ty::Bool,
                    },
                    contracts: Vec::new(),
                    preconditions: Vec::new(),
                    postconditions: Vec::new(),
                    spec: trust_types::FunctionSpec::default(),
                };

                match trust_proof_cert::generate_certificate(&func, &func_result.results) {
                    Ok(cert) => {
                        // Count proved VCs from the generated certificate.
                        let proved_count = cert.vc_entries.iter().filter(|e| e.proved).count();
                        let failed_count = cert.vc_entries.len() - proved_count;
                        generated += proved_count;
                        skipped += failed_count;
                    }
                    Err(_) => {
                        // Certificate generation failed -- count all as skipped.
                        skipped += func_result.results.len();
                    }
                }

                // tRust #545: Attempt lean5 kernel certification for proved VCs
                // that carry z4 proof certificates. This upgrades Trusted -> Certified.
                for (vc, result) in &func_result.results {
                    if let trust_types::VerificationResult::Proved {
                        proof_certificate: Some(ref cert_bytes),
                        ..
                    } = result
                    {
                        // Try to translate z4 proof certificate to a SolverProof,
                        // then run lean5 certification.
                        match attempt_lean5_certification(
                            &self.lean5_pipeline,
                            vc,
                            result,
                            cert_bytes,
                        ) {
                            Lean5Outcome::Certified => lean5_certified += 1,
                            Lean5Outcome::Rejected => lean5_rejected += 1,
                            Lean5Outcome::Skipped => {}
                        }
                    }
                }
            }

            return CertificationSummary {
                certificates_generated: generated,
                certificates_skipped: skipped,
                lean5_certified,
                lean5_rejected,
                time_ms: start.elapsed().as_millis() as u64,
            };
        }

        // Fallback: no detailed results, count from frontier.
        let proved = output.frontier.trusted + output.frontier.certified;
        let total = output.vcs_dispatched;
        CertificationSummary {
            certificates_generated: proved as usize,
            certificates_skipped: total.saturating_sub(proved as usize),
            lean5_certified: 0,
            lean5_rejected: 0,
            time_ms: start.elapsed().as_millis() as u64,
        }
    }
}

/// tRust #545: Outcome of a lean5 certification attempt.
enum Lean5Outcome {
    /// lean5 kernel confirmed the proof -- upgrades to Certified.
    Certified,
    /// lean5 rejected the proof term -- keeps original level.
    Rejected,
    /// Certification was not attempted (e.g., bytes not a valid z4 proof).
    Skipped,
}

/// tRust #545: Attempt lean5 kernel certification for a proved VC.
///
/// Tries two paths:
/// 1. Parse `cert_bytes` as a `Z4ProofCertificate` and translate to `SolverProof`,
///    then use `certify_from_solver_proof` for theory-aware lean5 certification.
/// 2. If parsing fails, fall back to raw proof term certification via `certify()`.
fn attempt_lean5_certification(
    pipeline: &trust_lean5::CertificationPipeline,
    vc: &trust_types::VerificationCondition,
    result: &trust_types::VerificationResult,
    cert_bytes: &[u8],
) -> Lean5Outcome {
    // Path 1: Try to deserialize as Z4ProofCertificate for structured translation.
    if let Ok(z4_cert) = bincode::deserialize::<
        trust_proof_cert::z4_certificate::Z4ProofCertificate,
    >(cert_bytes)
    {
        if let Ok(solver_proof) = trust_lean5::translate_z4_proof(&z4_cert) {
            let cert_result =
                pipeline.certify_from_solver_proof(vc, result, &solver_proof);
            return match cert_result {
                trust_lean5::CertificationResult::Certified { .. } => Lean5Outcome::Certified,
                // tRust #758: Trusted = kernel did NOT confirm, keep original level.
                trust_lean5::CertificationResult::Trusted { .. } => Lean5Outcome::Skipped,
                trust_lean5::CertificationResult::Rejected { .. } => Lean5Outcome::Rejected,
                trust_lean5::CertificationResult::Skipped { .. } | _ => Lean5Outcome::Skipped,
            };
        }
    }

    // Path 2: Fall back to raw proof term certification.
    let cert_result = pipeline.certify(vc, result, cert_bytes.to_vec());
    match cert_result {
        trust_lean5::CertificationResult::Certified { .. } => Lean5Outcome::Certified,
        // tRust #758: Trusted = kernel did NOT confirm, keep original level.
        trust_lean5::CertificationResult::Trusted { .. } => Lean5Outcome::Skipped,
        trust_lean5::CertificationResult::Rejected { .. } => Lean5Outcome::Rejected,
        trust_lean5::CertificationResult::Skipped { .. } | _ => Lean5Outcome::Skipped,
    }
}

