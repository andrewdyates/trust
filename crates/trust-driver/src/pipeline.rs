/// Configuration for the verification pipeline.
#[derive(Debug, Clone)]
pub(crate) struct PipelineConfig {
    /// Maximum number of loop iterations.
    pub max_iterations: u32,
    /// Number of consecutive stable rounds before declaring convergence.
    pub stable_round_limit: usize,
    /// Whether backprop is allowed to modify source files.
    pub allow_rewrite: bool,
    /// Whether to run CEGAR refinement on failed VCs.
    pub enable_cegar: bool,
    /// Whether to generate proof certificates for proved VCs.
    pub enable_certification: bool,
    /// Stall detection threshold for convergence verdict.
    pub stall_threshold: usize,
    /// Whether to generate tests from counterexamples on failed VCs.
    pub enable_testgen: bool,
    /// Output directory for generated test files.
    pub testgen_output_dir: std::path::PathBuf,
}

impl Default for PipelineConfig {
    fn default() -> Self {
        Self {
            max_iterations: 8,
            stable_round_limit: 2,
            allow_rewrite: true,
            enable_cegar: true,
            enable_certification: true,
            stall_threshold: 3,
            enable_testgen: false,
            testgen_output_dir: std::path::PathBuf::from("trust_generated_tests"),
        }
    }
}

