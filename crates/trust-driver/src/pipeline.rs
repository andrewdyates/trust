/// Why the pipeline terminated.
#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) enum PipelineOutcome {
    /// All VCs proved (possibly after CEGAR refinement).
    AllProved {
        iterations: u32,
        certificates: usize,
    },
    /// The proof frontier converged (stable for N rounds).
    Converged {
        iterations: u32,
        stable_rounds: usize,
        vcs_failed: usize,
    },
    /// Hit the iteration limit.
    IterationLimit {
        iterations: u32,
    },
    /// Proof frontier regressed.
    Regressed {
        iterations: u32,
    },
    /// Strengthen produced no proposals.
    NoProposals {
        iterations: u32,
        vcs_failed: usize,
    },
    /// Convergence monitor detected a stall.
    Stalled {
        iterations: u32,
        stale_iterations: usize,
        vcs_failed: usize,
    },
}

/// Result of running the pipeline.
#[derive(Debug, Clone)]
pub(crate) struct PipelineResult {
    /// Why the pipeline stopped.
    pub outcome: PipelineOutcome,
    /// Final proof frontier.
    pub final_frontier: ProofFrontier,
    /// Per-iteration history.
    pub history: Vec<PipelineIterationRecord>,
    /// Total certificates generated across all iterations.
    pub total_certificates: usize,
    /// Total CEGAR refinement iterations across all pipeline iterations.
    pub total_cegar_refinements: usize,
    /// Total tests generated from counterexamples across all iterations.
    pub total_tests_generated: usize,
}

