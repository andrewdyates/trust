/// Trait for CEGAR refinement after verification failures.
///
/// Implementations receive the verify output and attempt to refine the
/// abstraction to eliminate spurious counterexamples. The refined output
/// replaces the original verify output in the pipeline.
pub(crate) trait CegarStage {
    /// Attempt CEGAR refinement on the given verify output.
    ///
    /// Returns the (possibly improved) verify output and CEGAR statistics.
    fn refine(&self, source_path: &Path, output: VerifyOutput) -> (VerifyOutput, CegarStats);
}

/// Trait for proof certificate generation after successful proofs.
///
/// Implementations receive the final verify output (after CEGAR refinement)
/// and produce a certificate summary. The certificate is advisory — it does
/// not affect the pipeline flow.
pub(crate) trait CertificationStage {
    /// Generate certificates for proved VCs.
    ///
    /// Returns a summary of generated certificates.
    fn certify(&self, source_path: &Path, output: &VerifyOutput) -> CertificationSummary;
}

/// Trait for convergence monitoring across loop iterations.
///
/// Implementations track the proof frontier across iterations and decide
/// whether the loop has converged, stalled, or regressed. This is already
/// partially integrated via ConvergenceTracker, but this trait allows
/// plugging in alternative convergence detectors.
pub(crate) trait ConvergenceStage {
    /// Observe a new iteration's verify output and return the convergence decision.
    fn observe(
        &mut self,
        iteration: u32,
        output: &VerifyOutput,
    ) -> ConvergenceDecision;

    /// Get the high-level convergence verdict.
    fn verdict(&self) -> ConvergenceVerdict;

    /// Get the number of consecutive stable iterations.
    fn consecutive_stable_count(&self) -> usize;
}

