    // -- Mock stages --------------------------------------------------------

    /// A CEGAR stage that improves the verify output by reducing failures.
    struct MockCegarStage {
        reduction: usize,
    }

    impl CegarStage for MockCegarStage {
        fn refine(&self, _: &Path, mut output: VerifyOutput) -> (VerifyOutput, CegarStats) {
            let reduced = self.reduction.min(output.vcs_failed);
            output.vcs_failed -= reduced;
            output.frontier.failed = output.frontier.failed.saturating_sub(reduced as u32);
            output.frontier.trusted += reduced as u32;
            let stats = CegarStats {
                refinement_iterations: 1,
                spurious_eliminated: reduced,
                real_counterexamples: output.vcs_failed,
                predicates_added: reduced,
                hit_limit: false,
            };
            (output, stats)
        }
    }

    /// A certification stage that counts proved VCs.
    struct MockCertificationStage;

    impl CertificationStage for MockCertificationStage {
        fn certify(&self, _: &Path, output: &VerifyOutput) -> CertificationSummary {
            CertificationSummary {
                certificates_generated: output.frontier.trusted as usize,
                certificates_skipped: output.vcs_failed,
                lean5_certified: 0,
                lean5_rejected: 0,
                time_ms: 1,
            }
        }
    }

    // -- Helpers ------------------------------------------------------------

    fn frontier(trusted: u32, failed: u32) -> ProofFrontier {
        ProofFrontier {
            trusted,
            certified: 0,
            runtime_checked: 0,
            failed,
            unknown: 0,
        }
    }

    fn verify_output(trusted: u32, failed: u32) -> VerifyOutput {
        VerifyOutput {
            frontier: frontier(trusted, failed),
            fingerprint: None,
            vcs_dispatched: (trusted + failed) as usize,
            vcs_failed: failed as usize,
            detailed_results: None,
        }
    }

