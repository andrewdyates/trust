/// Modular verification pipeline that orchestrates CEGAR, convergence
/// detection, and proof certification around the core verify-strengthen-backprop
/// loop.
///
/// The pipeline runs:
///   1. **Verify** via the provided VerifyPhase
///   2. **CEGAR** refinement (optional, on failures)
///   3. **Convergence** check
///   4. **Strengthen + Backprop** (on failures, if not converged)
///   5. **Certify** (optional, on proved VCs)
///
/// Each stage is trait-dispatched, allowing callers to swap implementations.
pub(crate) struct Pipeline<'a> {
    config: PipelineConfig,
    verify: &'a dyn VerifyPhase,
    strengthen: &'a dyn StrengthenPhase,
    backprop: &'a dyn BackpropPhase,
    cegar: Option<Box<dyn CegarStage + 'a>>,
    convergence: Box<dyn ConvergenceStage + 'a>,
    certification: Option<Box<dyn CertificationStage + 'a>>,
    testgen: Option<Box<dyn TestgenStage + 'a>>,
}

impl<'a> Pipeline<'a> {
    /// Create a new pipeline with default CEGAR, convergence, and certification stages.
    #[must_use]
    pub(crate) fn new(
        config: PipelineConfig,
        verify: &'a dyn VerifyPhase,
        strengthen: &'a dyn StrengthenPhase,
        backprop: &'a dyn BackpropPhase,
    ) -> Self {
        let cegar: Option<Box<dyn CegarStage>> = if config.enable_cegar {
            Some(Box::new(DefaultCegarStage::new(CegarPhaseConfig::default())))
        } else {
            None
        };

        let convergence: Box<dyn ConvergenceStage> = Box::new(DefaultConvergenceStage::new(
            config.stable_round_limit,
            config.max_iterations,
            config.stall_threshold,
        ));

        let certification: Option<Box<dyn CertificationStage>> = if config.enable_certification {
            Some(Box::new(DefaultCertificationStage::new()))
        } else {
            None
        };

        let testgen: Option<Box<dyn TestgenStage>> = if config.enable_testgen {
            Some(Box::new(DefaultTestgenStage::new(TestgenConfig {
                output_dir: config.testgen_output_dir.clone(),
                from_counterexamples: true,
                from_unproved: false,
            })))
        } else {
            None
        };

        Self {
            config,
            verify,
            strengthen,
            backprop,
            cegar,
            convergence,
            certification,
            testgen,
        }
    }

    /// Replace the CEGAR stage with a custom implementation.
    #[must_use]
    pub(crate) fn with_cegar(mut self, cegar: Box<dyn CegarStage + 'a>) -> Self {
        self.cegar = Some(cegar);
        self
    }

    /// Replace the convergence stage with a custom implementation.
    #[must_use]
    pub(crate) fn with_convergence(mut self, convergence: Box<dyn ConvergenceStage + 'a>) -> Self {
        self.convergence = convergence;
        self
    }

    /// Replace the certification stage with a custom implementation.
    #[must_use]
    pub(crate) fn with_certification(mut self, certification: Box<dyn CertificationStage + 'a>) -> Self {
        self.certification = Some(certification);
        self
    }

    /// Disable the CEGAR stage.
    #[must_use]
    pub(crate) fn without_cegar(mut self) -> Self {
        self.cegar = None;
        self
    }

    /// Disable the certification stage.
    #[must_use]
    pub(crate) fn without_certification(mut self) -> Self {
        self.certification = None;
        self
    }

    /// Replace the test generation stage with a custom implementation.
    #[must_use]
    pub(crate) fn with_testgen(mut self, testgen: Box<dyn TestgenStage + 'a>) -> Self {
        self.testgen = Some(testgen);
        self
    }

    /// Disable the test generation stage.
    #[must_use]
    pub(crate) fn without_testgen(mut self) -> Self {
        self.testgen = None;
        self
    }

    /// Run the full pipeline to completion.
    pub(crate) fn run(&mut self, source_path: &Path) -> PipelineResult {
        let mut history = Vec::new();
        let mut iteration: u32 = 0;
        let mut total_certificates: usize = 0;
        let mut total_cegar_refinements: usize = 0;
        let mut total_tests_generated: usize = 0;

        loop {
            let start = Instant::now();

            // Phase 1: Verify
            let mut v_out = self.verify.verify(source_path);

            // Phase 2: CEGAR refinement (optional, on failures)
            let cegar_stats = if v_out.vcs_failed > 0 {
                if let Some(ref cegar) = self.cegar {
                    let (refined, stats) = cegar.refine(source_path, v_out);
                    v_out = refined;
                    total_cegar_refinements += stats.refinement_iterations;
                    Some(stats)
                } else {
                    None
                }
            } else {
                None
            };

            // Phase 2.5: Test generation from counterexamples (optional, on failures)
            let testgen_output = if v_out.vcs_failed > 0 {
                if let Some(ref testgen) = self.testgen {
                    let output = testgen.generate(&v_out);
                    total_tests_generated += output.cex_tests_generated;
                    Some(output)
                } else {
                    None
                }
            } else {
                None
            };

            // Phase 3: Convergence check
            let decision = self.convergence.observe(iteration, &v_out);

            // Phase 4: Certification (optional, for proved VCs)
            let cert_summary = self.certification.as_ref().map(|cert| {
                let summary = cert.certify(source_path, &v_out);
                total_certificates += summary.certificates_generated;
                summary
            });

            // Early exit: all proved
            if v_out.vcs_failed == 0 {
                history.push(PipelineIterationRecord {
                    iteration,
                    frontier: v_out.frontier.clone(),
                    vcs_failed: 0,
                    cegar_stats,
                    convergence: decision,
                    certification: cert_summary,
                    proposals: 0,
                    applied: 0,
                    testgen: testgen_output.clone(),
                    elapsed: start.elapsed(),
                });
                return PipelineResult {
                    outcome: PipelineOutcome::AllProved {
                        iterations: iteration + 1,
                        certificates: total_certificates,
                    },
                    final_frontier: v_out.frontier,
                    history,
                    total_certificates,
                    total_cegar_refinements,
                    total_tests_generated,
                };
            }

            // Check convergence/regression/limit
            match &decision {
                ConvergenceDecision::Converged { stable_rounds, .. } => {
                    history.push(PipelineIterationRecord {
                        iteration,
                        frontier: v_out.frontier.clone(),
                        vcs_failed: v_out.vcs_failed,
                        cegar_stats,
                        convergence: decision.clone(),
                        certification: cert_summary,
                        proposals: 0,
                        applied: 0,
                        testgen: testgen_output.clone(),
                        elapsed: start.elapsed(),
                    });
                    return PipelineResult {
                        outcome: PipelineOutcome::Converged {
                            iterations: iteration + 1,
                            stable_rounds: *stable_rounds,
                            vcs_failed: v_out.vcs_failed,
                        },
                        final_frontier: v_out.frontier,
                        history,
                        total_certificates,
                        total_cegar_refinements,
                        total_tests_generated,
                    };
                }
                ConvergenceDecision::IterationLimitReached { .. } => {
                    // Check for stall before returning IterationLimit.
                    let verdict = self.convergence.verdict();
                    history.push(PipelineIterationRecord {
                        iteration,
                        frontier: v_out.frontier.clone(),
                        vcs_failed: v_out.vcs_failed,
                        cegar_stats,
                        convergence: decision.clone(),
                        certification: cert_summary,
                        proposals: 0,
                        applied: 0,
                        testgen: testgen_output.clone(),
                        elapsed: start.elapsed(),
                    });
                    if let ConvergenceVerdict::Stalled { stale_iterations } = verdict {
                        return PipelineResult {
                            outcome: PipelineOutcome::Stalled {
                                iterations: iteration + 1,
                                stale_iterations,
                                vcs_failed: v_out.vcs_failed,
                            },
                            final_frontier: v_out.frontier,
                            history,
                            total_certificates,
                            total_cegar_refinements,
                            total_tests_generated,
                        };
                    }
                    return PipelineResult {
                        outcome: PipelineOutcome::IterationLimit {
                            iterations: iteration + 1,
                        },
                        final_frontier: v_out.frontier,
                        history,
                        total_certificates,
                        total_cegar_refinements,
                        total_tests_generated,
                    };
                }
                ConvergenceDecision::Regressed { .. } => {
                    history.push(PipelineIterationRecord {
                        iteration,
                        frontier: v_out.frontier.clone(),
                        vcs_failed: v_out.vcs_failed,
                        cegar_stats,
                        convergence: decision.clone(),
                        certification: cert_summary,
                        proposals: 0,
                        applied: 0,
                        testgen: testgen_output.clone(),
                        elapsed: start.elapsed(),
                    });
                    return PipelineResult {
                        outcome: PipelineOutcome::Regressed {
                            iterations: iteration + 1,
                        },
                        final_frontier: v_out.frontier,
                        history,
                        total_certificates,
                        total_cegar_refinements,
                        total_tests_generated,
                    };
                }
                ConvergenceDecision::Continue { .. } => {
                    // Check for stall via convergence verdict.
                    let verdict = self.convergence.verdict();
                    if let ConvergenceVerdict::Stalled { stale_iterations } = verdict {
                        history.push(PipelineIterationRecord {
                            iteration,
                            frontier: v_out.frontier.clone(),
                            vcs_failed: v_out.vcs_failed,
                            cegar_stats,
                            convergence: decision.clone(),
                            certification: cert_summary,
                            proposals: 0,
                            applied: 0,
                            testgen: testgen_output.clone(),
                            elapsed: start.elapsed(),
                        });
                        return PipelineResult {
                            outcome: PipelineOutcome::Stalled {
                                iterations: iteration + 1,
                                stale_iterations,
                                vcs_failed: v_out.vcs_failed,
                            },
                            final_frontier: v_out.frontier,
                            history,
                            total_certificates,
                            total_cegar_refinements,
                            total_tests_generated,
                        };
                    }
                    // Continue to strengthen + backprop.
                }
            }

            // Phase 5: Strengthen
            let s_out = self.strengthen.strengthen(source_path, &v_out);

            if !s_out.has_proposals {
                history.push(PipelineIterationRecord {
                    iteration,
                    frontier: v_out.frontier.clone(),
                    vcs_failed: v_out.vcs_failed,
                    cegar_stats,
                    convergence: decision,
                    certification: cert_summary,
                    proposals: 0,
                    applied: 0,
                    testgen: testgen_output.clone(),
                    elapsed: start.elapsed(),
                });
                return PipelineResult {
                    outcome: PipelineOutcome::NoProposals {
                        iterations: iteration + 1,
                        vcs_failed: v_out.vcs_failed,
                    },
                    final_frontier: v_out.frontier,
                    history,
                    total_certificates,
                    total_cegar_refinements,
                    total_tests_generated,
                };
            }

            // Phase 6: Backprop
            let b_out = if self.config.allow_rewrite {
                self.backprop.backpropagate(source_path, &s_out.proposals)
            } else {
                BackpropOutput {
                    applied: 0,
                    skipped: s_out.proposals.len(),
                }
            };

            history.push(PipelineIterationRecord {
                iteration,
                frontier: v_out.frontier.clone(),
                vcs_failed: v_out.vcs_failed,
                cegar_stats,
                convergence: decision,
                certification: cert_summary,
                proposals: s_out.proposals.len(),
                applied: b_out.applied,
                testgen: testgen_output.clone(),
                elapsed: start.elapsed(),
            });

            // If nothing was applied and rewrite is enabled, treat as no-proposals.
            if self.config.allow_rewrite && b_out.applied == 0 {
                return PipelineResult {
                    outcome: PipelineOutcome::NoProposals {
                        iterations: iteration + 1,
                        vcs_failed: v_out.vcs_failed,
                    },
                    final_frontier: v_out.frontier,
                    history,
                    total_certificates,
                    total_cegar_refinements,
                    total_tests_generated,
                };
            }

            iteration += 1;
        }
    }
}

