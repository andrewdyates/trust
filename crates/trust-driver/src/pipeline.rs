// trust-driver/pipeline.rs: Modular verification pipeline orchestrating CEGAR,
// proof certification, and convergence detection.
//
// Wires trust-cegar, trust-proof-cert, and trust-convergence into the main
// verification driver loop as optional, trait-dispatched pipeline stages.
//
// Pipeline flow:
//   1. Verify (existing VerifyPhase)
//   2. CEGAR refinement (on failures, optional)
//   3. Convergence check (ConvergenceTracker, always)
//   4. Strengthen + Backprop (existing phases)
//   5. Certificate generation (on success, optional)
//
// Each stage is modular: callers enable/disable stages via PipelineConfig.
// Stages are trait-based so they can be swapped or mocked in tests.
//
// Part of #415: Pipeline integration for trust-cegar, trust-proof-cert, trust-convergence
//
// Author: Andrew Yates <andrew@andrewdyates.com>
// Copyright 2026 Andrew Yates | License: Apache 2.0

