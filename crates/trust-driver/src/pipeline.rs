/// Summary of certificates generated by the certification stage.
#[derive(Debug, Clone, Default)]
pub(crate) struct CertificationSummary {
    /// Number of VCs for which certificates were generated.
    pub certificates_generated: usize,
    /// Number of VCs that could not be certified (e.g., failed VCs).
    pub certificates_skipped: usize,
    /// tRust #545: Number of VCs upgraded to Certified via lean5 kernel verification.
    pub lean5_certified: usize,
    /// tRust #545: Number of VCs where lean5 certification was attempted but failed.
    pub lean5_rejected: usize,
    /// Total time spent generating certificates.
    pub time_ms: u64,
}

/// Record of a single pipeline iteration.
#[derive(Debug, Clone)]
pub(crate) struct PipelineIterationRecord {
    /// Zero-based iteration index.
    pub iteration: u32,
    /// Proof frontier after verification (and CEGAR refinement).
    pub frontier: ProofFrontier,
    /// Number of VCs that failed after all refinement.
    pub vcs_failed: usize,
    /// CEGAR statistics for this iteration (if CEGAR was run).
    pub cegar_stats: Option<CegarStats>,
    /// Convergence decision from the convergence stage.
    pub convergence: ConvergenceDecision,
    /// Certification summary (if certification was run).
    pub certification: Option<CertificationSummary>,
    /// Number of spec proposals generated.
    pub proposals: usize,
    /// Number of proposals applied by backprop.
    pub applied: usize,
    /// Test generation output (if testgen was run).
    pub testgen: Option<TestgenOutput>,
    /// Wall-clock time for this iteration.
    pub elapsed: Duration,
}

